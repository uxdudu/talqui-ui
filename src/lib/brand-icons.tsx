export type BrandIconKey =
  | "instagram"
  | "whatsapp"
  | "messenger"
  | "telegram"
  | "slack";

const BRAND_ICONS: Record<
  BrandIconKey,
  ComponentType<{ className?: string } & SVGProps<SVGSVGElement>>
> = {
  instagram: InstagramIcon,
  whatsapp: WhatsAppIcon,
  messenger: MessengerIcon,
  telegram: TelegramIcon,
  slack: SlackIcon,
};

/** Mapeia extension.id (ou prefixo) para a chave do ícone no banco. */
function getBrandKey(extensionId: string): BrandIconKey | null {
  if (extensionId === "instagram") return "instagram";
  if (extensionId === "messenger") return "messenger";
  if (extensionId === "telegram") return "telegram";
  if (extensionId === "slack") return "slack";
  if (
    extensionId.startsWith("wa-") ||
    extensionId.startsWith("whatsapp") ||
    extensionId === "whatsapp-api" ||
    extensionId === "whatsapp-oficial" ||
    extensionId === "whatsapp-business"
  )
    return "whatsapp";
  return null;
}

/**
 * Retorna o componente de ícone SVG para a extensão, ou null para usar logoUrl (img).
 * Uso: const Logo = getExtensionLogo(extension.id); Logo ? <Logo className="h-8 w-8" /> : <img src={extension.logoUrl} />
 */
export function getExtensionLogo(extensionId: string) {
  const key = getBrandKey(extensionId);
  return key ? BRAND_ICONS[key] : null;
}
